const CACHE_DIR = path.join(__dirname, 'cache');
const RATING_CACHE_FILE = path.join(CACHE_DIR, 'ratings.json');
const REAL_RATING_CACHE_FILE = path.join(CACHE_DIR, 'real_ratings.json');
const CACHE_DURATION = 5 * 60 * 1000; 


fs.mkdir(CACHE_DIR, { recursive: true }).catch(err => {
  logger.error('Failed to create cache directory:', err);
});


async function getCachedRatings() {
  try {
    
    const cacheData = await fs.readFile(RATING_CACHE_FILE, 'utf8');
    const parsed = JSON.parse(cacheData);

    
    if (Date.now() - parsed.timestamp < CACHE_DURATION) {
      logger.info('âœ… Loaded dynamic rating data from cache');
      return parsed.data;
    }
  } catch (error) {
    logger.info('Dynamic cache not found or invalid, trying realistic cache...');
  }

  try {
    
    const realCacheData = await fs.readFile(REAL_RATING_CACHE_FILE, 'utf8');
    const realParsed = JSON.parse(realCacheData);
    logger.info('âœ… Loaded realistic rating data from cache');
    return realParsed.data;
  } catch (error) {
    
    logger.info('No rating cache available');
  }
  return null;
}

async function setCachedRatings(ratings) {
  try {
    const cacheData = {
      timestamp: Date.now(),
      data: ratings
    };
    await fs.writeFile(RATING_CACHE_FILE, JSON.stringify(cacheData, null, 2));
    logger.info('Rating cache updated');
  } catch (error) {
    logger.error('Failed to update rating cache:', error);
  }
}

async function setRealCachedRatings(ratings) {
  try {
    const cacheData = {
      timestamp: Date.now(),
      data: ratings,
      total: ratings.length,
      generated: new Date().toISOString(),
      description: 'Realistic test data for ENT rating system'
    };
    await fs.writeFile(REAL_RATING_CACHE_FILE, JSON.stringify(cacheData, null, 2));
    logger.info('Real rating cache updated');
  } catch (error) {
    logger.error('Failed to update real rating cache:', error);
  }
}

async function updateCachedRating(userId, newScore, newTotalTests, newAverageScore) {
  try {
    
    let currentRatings = await getCachedRatings();
    if (!currentRatings) {
      logger.info('No cache to update, skipping rating update');
      return;
    }

    
    const userIndex = currentRatings.findIndex(user => user.id === userId);
    if (userIndex !== -1) {
      
      currentRatings[userIndex].score = newScore;
      currentRatings[userIndex].bestENT = newScore;
      currentRatings[userIndex].totalTests = newTotalTests;
      currentRatings[userIndex].averageScore = Math.round(newAverageScore * 100) / 100;
    } else {
      
      logger.warn(`User ${userId} not found in cache for rating update`);
      return;
    }

    
    currentRatings.sort((a, b) => (b.score || 0) - (a.score || 0));

    
    await setRealCachedRatings(currentRatings);
    logger.info(`Rating updated for user ${userId}: score=${newScore}`);
  } catch (error) {
    logger.error('Failed to update cached rating:', error);
  }
}


