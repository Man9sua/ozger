  logger.info(`User connected: ${username} (${userId})`);

  
  connectedUsers.set(userId, {
    id: userId,
    username: username,
    email: socket.user.email,
    connectedAt: new Date().toISOString(),
    socketId: socket.id
  });

  
  socket.broadcast.emit('user_online', {
    userId: userId,
    username: username,
    timestamp: new Date().toISOString()
  });

  
  const onlineUsers = Array.from(connectedUsers.values()).filter(user => user.id !== userId);
  socket.emit('online_users', onlineUsers);

  
  socket.on('join_rating', () => {
    socket.join('rating_room');
    logger.info(`User ${username} joined rating room`);
  });

  
