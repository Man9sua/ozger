app.get('/api/rating', async (req, res) => {
  
  res.header('Access-Control-Allow-Origin', '*');
  res.header('Access-Control-Allow-Methods', 'GET, POST, PUT, DELETE, OPTIONS');
  res.header('Access-Control-Allow-Headers', 'Content-Type, Authorization, X-Requested-With');

  const startTime = Date.now();
  const { type = 'ent', subject, scope = 'all' } = req.query;

  try {
    logger.info(`Rating request: type=${type}, subject=${subject}, scope=${scope}`);

    
    let ratings = await getCachedRatings();

    if (!ratings) {
      logger.info('Cache miss - fetching from Supabase...');

      
      try {
        const { data, error } = await supabase
          .from('user_stats')
          .select(`
            user_id,
            best_ent_score,
            total_tests_completed,
            average_score,
            profiles!inner (
              username,
              school,
              class_number,
              class_letter,
              country,
              city
            )
          `)
          .not('best_ent_score', 'is', null)
          .order('best_ent_score', { ascending: false })
          .limit(50);

        if (!error && data && data.length > 0) {
          ratings = data.map(stat => ({
            id: stat.user_id,
            username: stat.profiles.username || 'Unknown',
            school: stat.profiles.school || 'Unknown School',
            class: stat.profiles.class_number ?
              `${stat.profiles.class_number}${stat.profiles.class_letter || ''}` : 'Unknown',
            score: stat.best_ent_score || 0,
            bestENT: stat.best_ent_score || 0,
            totalTests: stat.total_tests_completed || 0,
            averageScore: Math.round((stat.average_score || 0) * 100) / 100,
            country: stat.profiles.country || 'kz',
            city: stat.profiles.city || 'unknown'
          }));

          
          setCachedRatings(ratings).catch(err => logger.error('Cache write error:', err));
        } else {
          logger.warn('Supabase query returned no data or error:', error?.message);
          ratings = await getFallbackRatings();
        }
      } catch (supabaseError) {
        logger.error('Supabase connection error:', supabaseError.message);
        ratings = await getFallbackRatings();
      }
    }

    
    let filteredRatings = ratings;

    
    if (subject && subject !== 'all') {
      
      
      logger.info(`Filtering by subject: ${subject} (not implemented yet)`);
    }

    
    if (scope === 'school') {
      if (req.user) {
        
        try {
          const { data: userProfile } = await supabase
            .from('profiles')
            .select('school')
            .eq('user_id', req.user.id)
            .single();

          if (userProfile?.school) {
            filteredRatings = ratings.filter(user => user.school === userProfile.school);
            logger.info(`Filtered by user's school: ${userProfile.school}, results: ${filteredRatings.length}`);
          }
        } catch (error) {
          logger.warn('Could not filter by authenticated user school:', error.message);
        }
      } else {
        
        filteredRatings = ratings.filter(user => user.school === 'Dostyq School');
        logger.info(`Demo filter by school "Dostyq School", results: ${filteredRatings.length}`);
      }
    } else if (scope === 'class') {
      if (req.user) {
        
        try {
          const { data: userProfile } = await supabase
            .from('profiles')
            .select('school, class_number, class_letter')
            .eq('user_id', req.user.id)
            .single();

          if (userProfile?.school && userProfile?.class_number) {
            const userClass = `${userProfile.class_number}${userProfile.class_letter || ''}`;
            filteredRatings = ratings.filter(user =>
              user.school === userProfile.school && user.class === userClass
            );
            logger.info(`Filtered by user's class: ${userClass}, results: ${filteredRatings.length}`);
          }
        } catch (error) {
          logger.warn('Could not filter by authenticated user class:', error.message);
        }
      } else {
        
        filteredRatings = ratings.filter(user => user.class === '11Ð');
        logger.info(`Demo filter by class "11Ð", results: ${filteredRatings.length}`);
      }
    } else if (scope === 'profile') {
      
      
      logger.info('Profile scope selected - showing all for now');
    } else {
      logger.info('Showing all ratings');
    }

    
    if (type === 'average') {
      filteredRatings.sort((a, b) => (b.averageScore || 0) - (a.averageScore || 0));
      logger.info(`Sorted ${filteredRatings.length} ratings by average score`);
    } else if (type === 'total') {
      filteredRatings.sort((a, b) => (b.totalTests || 0) - (a.totalTests || 0));
      logger.info(`Sorted ${filteredRatings.length} ratings by total tests`);
    } else {
      
      filteredRatings.sort((a, b) => (b.score || 0) - (a.score || 0));
      logger.info(`Sorted ${filteredRatings.length} ratings by ENT score`);
    }

    const responseTime = Date.now() - startTime;
    logger.info(`Rating response: ${filteredRatings.length} items in ${responseTime}ms`);

    res.json({
      ratings: filteredRatings,
      total: filteredRatings.length,
      cached: true,
      responseTime: responseTime
    });
  } catch (error) {
    logger.error('Rating fetch error:', error);

    
    const emergencyData = await getFallbackRatings();
    const responseTime = Date.now() - startTime;

    res.json({
      ratings: emergencyData,
      total: emergencyData.length,
      error: 'Using emergency fallback data',
      responseTime: responseTime
    });
  }
});


